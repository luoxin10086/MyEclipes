package com.sys.model;

public class Admin {
	private String txtUser;//姓名
	private String txtPass;//密码
	private String txtRPass;//重复密码
	private String gen;//性别
	private String txtEmail;//邮箱
	private String year;//年
	private String month;//月
	private String day;//日

	public String getTxtUser() {
		return txtUser;
	}
	public void setTxtUser(String txtUser) {
		this.txtUser = txtUser;
	}
	public String getTxtPass() {
		return txtPass;
	}
	public void setTxtPass(String txtPass) {
		this.txtPass = txtPass;
	}
	public String getTxtRPass() {
		return txtRPass;
	}
	public void setTxtRPass(String txtRPass) {
		this.txtRPass = txtRPass;
	}
	public String getGen() {
		return gen;
	}
	public void setGen(String gen) {
		this.gen = gen;
	}
	public String getTxtEmail() {
		return txtEmail;
	}
	public void setTxtEmail(String txtEmail) {
		this.txtEmail = txtEmail;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	
}
